#include <iostream>
using namespace std;

int main()
{
    int i = 1; // yahan variable i ko 1 se start kiya gaya hai

    while(i <= 10) // jab tak i 10 se chota ya barabar hai loop chalta rahega
    {
        cout << i << endl; // current number screen par print karega
        i++; // i ki value 1 se barh jayegi (increment)
    }

    return 0; // program end
}
